#include <iostream>
double sr_arifm(int* arr, int n)
{

    double sum = 0;
    for (int i = 0; i < n; i++)
    {
        sum = sum + arr[i];
    }
    return sum / n;
}

int main()
{

    int n;
    std::cin >> n;
    int* arr = new int[n];
    for (int i = 0; i < n; i++)
    {
        std::cin >> arr[i];
    }
    double res = sr_arifm(arr, n);
    std::cout << "res = " << res;
    return 0;
}
